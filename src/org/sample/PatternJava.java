package org.sample;

public class PatternJava {

	public static void main(String[] args) {
for(int i=0;i<=10;i++) {
	for(int j=0;j<=1;j++) {
		System.out.print("*");
	}
	System.out.print("\n");
}
int k=0;
while(k<=10) {
	System.out.print("*");
}
System.out.println("using While loop= ");
System.out.println();
	}

}
